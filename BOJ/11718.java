import java.util.Scanner;

public class Main {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in).useDelimiter("\n");
        String s1 = sc.next();
        String s2 = sc.next();
        String s3 = sc.next();

        System.out.println(s1.trim());
        System.out.println(s2.trim());
        System.out.println(s3.trim());
    }
}
